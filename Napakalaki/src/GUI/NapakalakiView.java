    private void lucharActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_lucharActionPerformed
        
        this.luchar.setEnabled(false);
        
        CombatResult cr = this.napakalakiModel.developCombat();
        this.combate.setText("Resultado combate:    " + this.napakalakiModel.developCombat().toString());
        
        if (cr == CombatResult.LOSE || cr == CombatResult.LOSEANDCONVERT){
            this.napakalakiModel.getCurrentPlayer().setPendingBadConsequence(
                napakalakiModel.getCurrentMonster().getBadConsequence().adjustToFitTreasureList(
                napakalakiModel.getCurrentPlayer().getVisibleTreasures(),
                napakalakiModel.getCurrentPlayer().getHiddenTreasures() ));
        }else if ( cr == CombatResult.WINGAME){
            this.win.setText("Â¡El jugador " + this.napakalakiModel.getCurrentPlayer().getName() + " ha ganado!");
        
        }
        
        if(this.napakalakiModel.getCurrentPlayer().getPendingBad() != null){
            this.pend.setEnabled(true);    
            this.pend.setText("Pending Bad Consequence:  " + this.napakalakiModel.getCurrentPlayer().getPendingBad().toString());
            
        }else{ 
            this.pend.setText("");
        }
        
        this.playerView1.setPlayer(this.napakalakiModel.getCurrentPlayer());
        this.playerView1.setDescartarTesoro(true);
        this.playerView1.setDescartarTodo(true);
        this.playerView1.setMakeVisible(true);
        
        this.pend.setEditable(false);
        
      //  if(this.napakalakiModel.nextTurnAllowed())
            this.turno.setEnabled(true);
        
    }//GEN-LAST:event_lucharActionPerformed

