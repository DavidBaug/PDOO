    private void turnoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_turnoActionPerformed
     
        if(this.napakalakiModel.nextTurn()){

            this.pend.setText("");
            this.pend.setEnabled(false);
            
            this.combate.setText("Resultado combate:");
            
            this.playerView1.setMakeVisible(true);
            this.playerView1.setDescartarTesoro(false);
            this.playerView1.setDescartarTodo(false);

            this.monsterView1.setEnabled(false);
            
            this.setNapakalaki(napakalakiModel);
            this.turno.setEnabled(false);
            
        }else{
            this.error.setVisible(true);
            this.error.setEnabled(true);
            this.playerView1.setPlayer(this.napakalakiModel.getCurrentPlayer());
        }
        
        
        repaint();
    }//GEN-LAST:event_turnoActionPerformed

