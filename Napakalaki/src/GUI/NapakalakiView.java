    public void setNapakalaki(Napakalaki model){
        this.napakalakiModel = model;
        
        this.playerView1.setPlayer(this.napakalakiModel.getCurrentPlayer());
        this.playerView1.setNapakalaki(model);
        
        this.playerView1.setDescartarTesoro(true);
        this.playerView1.setDescartarTodo(true);
        this.playerView1.setMakeVisible(true);
        this.playerView1.setRobar(this.napakalakiModel.getCurrentPlayer().canISteal());
        
        this.monsterView1.setMonster(this.napakalakiModel.getCurrentMonster());
       // this.monsterView1.setOpaque(false);
        this.monsterView1.setVisible(false);
        
        this.mostrarMonstruo.setEnabled(true);
        this.luchar.setEnabled(false);
        this.turno.setEnabled(false);
        
        this.personaje = this.personaje%this.napakalakiModel.getPlayersSize();
        
        
        if(this.personaje == 0){
                this.person.setIcon(new javax.swing.ImageIcon(getClass().getResource("/GUI/imagenesNapakalaki/bulb.png")));
        }else if(this.personaje == 1){
                person.setIcon(new javax.swing.ImageIcon(getClass().getResource("/GUI/imagenesNapakalaki/cha.png")));
        }else if(this.personaje == 2){
                person.setIcon(new javax.swing.ImageIcon(getClass().getResource("/GUI/imagenesNapakalaki/squ.png")));
        }
        
        this.personaje++;
        
        this.pend.setEnabled(false);
        
        setVisible(true);
        
        repaint();
        revalidate();
        
    }
    
    
